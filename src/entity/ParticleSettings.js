/*
* Represents a container for settings to pass to a particle. 
*/
function ParticleSettings() {
    this.x = null;
    this.y = null;
    this.w = null;
    this.h = null;
    this.xVel = null;
    this.yVel = null;
    this.gravityType = null;
    this.collSetting = null;
    this.expire = null;
    this.lifeTime = null;
    this.color = null;
    this.sprite = null;
    this.rotationSpeed = null;
    this.fadeOutTime = null;
    this.fadeInTime = null;
};